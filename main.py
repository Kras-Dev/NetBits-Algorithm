from bit_mask import find_minimal_mask

def main() -> None:
# =====================================================================
# ТЕСТОВЫЙ НАБОР ДЛЯ ПРОВЕРКИ IP-АДРЕСОВ И СЛУЧАЕВ МЕЖВЕРСИОННОГО СРАВНЕНИЯ
# =====================================================================

    test_cases = [
        # 1. Корректные IPv4 (простые и граничные)
        {"ip1": "192.168.1.35", "ip2": "192.168.3.100", "desc": "Обычные частные IPv4"},
        {"ip1": "192.162.1.35", "ip2": "10.0.0.35", "desc": "Обычные разные IPv4"},
        {"ip1": "0.0.0.0", "ip2": "0.0.0.1", "desc": "Минимальные границы IPv4"},
        {"ip1": "255.255.255.254", "ip2": "255.255.255.255", "desc": "Максимальные границы IPv4"},
        {"ip1": "10.0.0.1", "ip2": "10.0.0.1", "desc": "Абсолютно идентичные IPv4"},

        # 2. Корректные IPv6 (разные типы сжатия и записи)
        {"ip1": "2001:db8:a::1", "ip2": "2001:db8:b::2", "desc": "IPv6 со сжатием '::' в середине"},
        {"ip1": "::1", "ip2": "::1", "desc": "Идентичные IPv6 Localhost (максимальное сжатие)"},
        {"ip1": "fe80::1111:2222:3333:4444", "ip2": "fe80::1111:2222:3333:5555", "desc": "IPv6 Link-Local со сжатием в начале"},
        {"ip1": "2001:0db8:0000:0000:0000:0000:0000:0001", "ip2": "2001:0db8:0000:0000:0000:0000:0000:0002", "desc": "Полностью развернутые IPv6"},
        {"ip1": "2001:db8::", "ip2": "2001:db8::1", "desc": "Сжатие нулей на конце адреса"},

        # 3. КРИТИЧЕСКИЕ ТЕСТЫ: Межверсионное сравнение (IPv4 против IPv6)
        {"ip1": "192.168.1.35", "ip2": "2001:db8:a::1", "desc": "ОШИБКА: IPv4 против IPv6 (сжатый)"},
        {"ip1": "2001:db8:0:0:0:0:0:1", "ip2": "127.0.0.1", "desc": "ОШИБКА: IPv6 (развернутый) против IPv4"},
        {"ip1": "::", "ip2": "0.0.0.0", "desc": "ОШИБКА: Пустой IPv6 против пустого IPv4"},

        # 4. КРИТИЧЕСКИЕ ТЕСТЫ: Некорректный синтаксис (должен падать в validate_ip)
        {"ip1": "192.168.1.01", "ip2": "192.168.1.2", "desc": "ОШИБКА IPv4: Ведущий ноль в октете '01'"},
        {"ip1": "256.100.10.1", "ip2": "192.168.1.1", "desc": "ОШИБКА IPv4: Число больше 255"},
        {"ip1": "192.168.1", "ip2": "192.168.1.1", "desc": "ОШИБКА IPv4: Всего 3 группы вместо 4"},
        {"ip1": "192.168.1.abc", "ip2": "192.168.1.1", "desc": "ОШИБКА IPv4: Буквы в адресе"},
        {"ip1": "2001:db8:::1", "ip2": "2001:db8::1", "desc": "ОШИБКА IPv6: Тройное двоеточие ':::'"},
        {"ip1": "2001::db8::1", "ip2": "2001:db8::1", "desc": "ОШИБКА IPv6: Два сжатия '::' в одном адресе"},
        {"ip1": "2001:db8:g::1", "ip2": "2001:db8::1", "desc": "ОШИБКА IPv6: Недопустимый символ 'g' (вне HEX)"},
        {"ip1": "2001:db888:a::1", "ip2": "2001:db8::1", "desc": "ОШИБКА IPv6: Группа длиннее 4 символов 'db888'"},
        {"ip1": "привет_мир", "ip2": "192.168.1.1", "desc": "ОШИБКА: Вообще не IP-адрес"},
    ]

    print(f"{'Описание теста':<55} | {'Результат / Исключение':<30}")
    print("-" * 90)

    for case in test_cases:
        desc = case["desc"]
        try:
            mask = find_minimal_mask(case["ip1"], case["ip2"])
            result_text = f"Успешно! Маска: /{mask}"
        except TypeError as e:
            result_text = f"TypeError: {e}"
        except ValueError as e:
            result_text = f"ValueError: {e}"
        except Exception as e:
            result_text = f"Неожиданная ошибка ({type(e).__name__}): {e}"
        
        print(f"{desc:<55} | {result_text}")


if __name__ == "__main__":
    main()